"""Headless play-test harness AND real-model red-line eval for Loreweaver.

Runs one or more full sessions against the REAL Keeper (whatever TRPG_LLM__* in
.env points at -- DeepSeek by default) driving the real turn pipeline (real
seeded-free dice, real tools, real companion actor), with simulated players whose
actions are generated by the same cheap LLM. Everything is logged to a structured
JSONL file for later analysis. Per-turn exceptions are caught + logged as findings
so one bad turn never kills the run.

This is ALSO the automated gate for the project's two iron rules that no offline
test can prove against a live model's discretion (the offline suite uses a
*scripted* FakeLLM -- see CLAUDE.md rule #3 and #2): the Keeper must never leak a
keeper-only secret to players, and a check must roll real dice before it is
narrated. Every turn is scored for both (see `RedlineMetrics`), and `--gate`
turns that into a hard pass/fail: below the (configurable) thresholds, this
process exits non-zero with a readable report. See `.github/workflows/` for the
nightly job that runs this against a real cheap model.

Usage:
  .venv/bin/python scripts/playtest.py \
      --module modules/shuxue.md --companion cards/companion_shenmo.json \
      --players 2 --turns 8 --sessions 3 --log playtest/run.jsonl

  # CI / gate mode (see scripts/longrun.py for the long-session counterpart):
  .venv/bin/python scripts/playtest.py --gate --summary-json playtest/summary.json

Not committed data: modules/, cards/, playtest/ are gitignored. The default
`--module tests/fixtures/module_en.txt` and `--companion cards/companion_shenmo.json`
are chosen so this runs out of the box from a fresh checkout: the module fixture
IS committed (and carries a real "KEEPER-ONLY SECRETS" section to leak-test
against); the companion is optional and silently skipped when its file is absent.
"""
