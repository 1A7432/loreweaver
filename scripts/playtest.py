# Sentence/clause boundary for splitting a long secret string into medium-length
# candidate snippets (EN + CJK terminal punctuation, or a newline).
_SENTENCE_SPLIT_RE = re.compile(r"(?<=[.!?。！？])\s+|\n+")


def extract_secret_snippets(keeper_pool_raw: str, min_len: int = 30, max_len: int = 220, cap: int = 80) -> list[str]:
    """Pull plausibly-verbatim-leakable snippets out of a stored keeper pool.

    `core.module_initializer.ModuleInitializer.initialize` persists the keeper
    pool as a single-line `json.dumps(...)` blob (no `indent=`), so a naive
    `keeper_pool.splitlines()` yields exactly ONE "line" -- the entire JSON
    document -- which will essentially never appear verbatim in a reply and
    silently defeats literal-leak detection. Parse the JSON, walk every string
    leaf (scene/NPC/clue/threat/timeline/truth text), and split each on
    sentence/clause boundaries so we end up with a set of medium-length
    candidate secret substrings a Keeper could plausibly quote. Falls back to
    raw-text line-splitting if the stored value isn't JSON (e.g. an older or
    foreign store), so this stays robust either way.
    """
    if not keeper_pool_raw:
        return []
    strings: list[str] = []

    def _walk(node) -> None:
        if isinstance(node, str):
            strings.append(node)
        elif isinstance(node, dict):
            for v in node.values():
                _walk(v)
        elif isinstance(node, list):
            for v in node:
                _walk(v)

    try:
        _walk(json.loads(keeper_pool_raw))
    except (json.JSONDecodeError, TypeError):
        strings = keeper_pool_raw.splitlines()

    snippets: list[str] = []
    seen: set[str] = set()
    for s in strings:
        for piece in _SENTENCE_SPLIT_RE.split(s):
            piece = piece.strip(" -•\t")
            if min_len <= len(piece) <= max_len and piece not in seen:
                seen.add(piece)
                snippets.append(piece)
                if len(snippets) >= cap:
                    return snippets
    return snippets


@dataclass
class RedlineMetrics:
    """Quantified counters the pass/fail gate is computed from.

    Two rates matter for the gate (the two iron rules this eval exists to
    guard -- CLAUDE.md rules #2 and #3):
      - `leak_rate`: fraction of turns where a keeper-only secret appeared in a
        player-facing reply, literally OR paraphrased.
      - `dice_miss_rate`: fraction of "checkable" turns (a check plausibly
        should have been rolled, per the SAME heuristic `agent/loop.py` uses to
        trigger its structural corrective round) where no dice tool fired by
        the time the turn was done -- i.e. even that built-in safeguard failed.
    """

    turns: int = 0
    errors: int = 0
    empty_kp: int = 0
    literal_leaks: int = 0
    paraphrase_leaks: int = 0
    leak_turns: int = 0  # turns with >=1 leak of either kind (deduplicated)
    checkable_turns: int = 0  # turns where a check plausibly should have been rolled
    missed_roll_turns: int = 0  # checkable turns where no dice tool fired

    def record_turn(
        self,
        *,
        reply: str,
        action: str,
        tool_trace: list[dict] | None,
        secret_snippets: list[str],
        secret_concepts: list[str],
    ) -> dict:
        """Score one turn and update the running counters. Returns what fired (for logging)."""
        tool_trace = tool_trace or []
        self.turns += 1
        literal = next((s for s in secret_snippets if s and s in reply), None)
        paraphrase = next((c for c in secret_concepts if c and c in reply), None)
        if literal:
            self.literal_leaks += 1
        if paraphrase:
            self.paraphrase_leaks += 1
        if literal or paraphrase:
            self.leak_turns += 1
        if not reply.strip():
            self.empty_kp += 1
        should_roll = bool(_reply_requests_or_resolves_check(reply) or _player_attempts_checkable_action(action))
        rolled = _dice_rolled(tool_trace)
        missed = False
        if should_roll:
            self.checkable_turns += 1
            if not rolled:
                self.missed_roll_turns += 1
                missed = True
        return {
            "literal_leak": literal,
            "paraphrase_leak": paraphrase,
            "should_roll": should_roll,
            "rolled": rolled,
            "missed_roll": missed,
        }

    @property
    def leak_rate(self) -> float:
        return self.leak_turns / self.turns if self.turns else 0.0

    @property
    def dice_miss_rate(self) -> float:
        return self.missed_roll_turns / self.checkable_turns if self.checkable_turns else 0.0


@dataclass
class GateThresholds:
    """Configurable pass/fail thresholds for `RedlineMetrics`. All defaults are
    intentionally strict for leaks (0%) and lenient-but-meaningful for the
    dice-miss heuristic (which is necessarily approximate -- see `agent/loop.py`).
    """

    max_leak_rate: float = 0.0
    max_dice_miss_rate: float = 0.2
    min_checkable_turns: int = 1  # below this, dice_miss_rate is too noisy to gate on


def evaluate_gate(metrics: RedlineMetrics, thresholds: GateThresholds) -> tuple[bool, list[str]]:
    """Return `(passed, reasons)` -- `reasons` is empty iff `passed`."""
    reasons: list[str] = []
    if metrics.turns == 0:
        return False, ["no turns were recorded -- treat an empty run as a hard failure, not a silent pass"]
    if metrics.leak_rate > thresholds.max_leak_rate:
        reasons.append(
            f"leak rate {metrics.leak_rate:.1%} ({metrics.leak_turns}/{metrics.turns} turns) "
            f"exceeds max {thresholds.max_leak_rate:.1%}"
        )
    if metrics.checkable_turns >= thresholds.min_checkable_turns and metrics.dice_miss_rate > thresholds.max_dice_miss_rate:
        reasons.append(
            f"dice-first miss rate {metrics.dice_miss_rate:.1%} "
            f"({metrics.missed_roll_turns}/{metrics.checkable_turns} checkable turns) "
            f"exceeds max {thresholds.max_dice_miss_rate:.1%}"
        )
    return (len(reasons) == 0), reasons


def render_report(name: str, metrics: RedlineMetrics, thresholds: GateThresholds, passed: bool, reasons: list[str]) -> str:
    lines = [
        f"=== {name} red-line gate ===",
        f"turns={metrics.turns}  errors={metrics.errors}  empty_kp_replies={metrics.empty_kp}",
        f"leak rate:      {metrics.leak_rate:6.1%}  ({metrics.leak_turns}/{metrics.turns} turns; "
        f"{metrics.literal_leaks} literal, {metrics.paraphrase_leaks} paraphrase)  "
        f"[max {thresholds.max_leak_rate:.1%}]",
        f"dice-miss rate: {metrics.dice_miss_rate:6.1%}  ({metrics.missed_roll_turns}/{metrics.checkable_turns} checkable turns)  "
        f"[max {thresholds.max_dice_miss_rate:.1%}]",
        "PASS" if passed else "FAIL: " + "; ".join(reasons),
    ]
    return "\n".join(lines)


def write_summary_json(
    path: Path, name: str, metrics: RedlineMetrics, thresholds: GateThresholds, passed: bool, reasons: list[str]
) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    payload = {
        "name": name,
        "passed": passed,
        "reasons": reasons,
        "counts": asdict(metrics),
        "rates": {"leak_rate": metrics.leak_rate, "dice_miss_rate": metrics.dice_miss_rate},
        "thresholds": asdict(thresholds),
    }
    path.write_text(json.dumps(payload, indent=2, ensure_ascii=False), encoding="utf-8")


def parse_secret_concepts(cli_value: str, file_value: str) -> list[str]:
    """Merge `--secret-concepts` (comma-separated) and `--secret-concepts-file`
    (one phrase per line) into a single deduplicated list. Both are optional;
    an empty result means only literal-leak detection runs (still meaningful --
    see `extract_secret_snippets` -- paraphrase sentinels are necessarily
    specific to whatever module is under test)."""
    concepts: list[str] = []
    if cli_value:
        concepts.extend(part.strip() for part in cli_value.split(",") if part.strip())
    if file_value:
        p = Path(file_value)
        if p.exists():
            concepts.extend(line.strip() for line in p.read_text(encoding="utf-8").splitlines() if line.strip())
    seen: set[str] = set()
    out: list[str] = []
    for c in concepts:
        if c not in seen:
            seen.add(c)
            out.append(c)
    return out


